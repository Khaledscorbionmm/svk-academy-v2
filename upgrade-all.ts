import fs from 'fs';
import path from 'path';

const tracksDir = path.join(process.cwd(), 'src/context/tracks');
const files = ['pythonData.ts', 'cyberData.ts', 'languageData.ts'];

for (const file of files) {
  const filePath = path.join(tracksDir, file);
  if (!fs.existsSync(filePath)) continue;
  let fileContent = fs.readFileSync(filePath, 'utf-8');
  
  const jsonMatch = fileContent.substring(fileContent.indexOf('['));
  const cleanJson = jsonMatch.replace(/;\s*export\s+default\s+\w+;\s*$/, '');
  
  const lessons = eval(cleanJson);
  
  let updated = false;
  for (const lesson of lessons) {
      if (!lesson.concept_context) {
          lesson.concept_context = '??? ????? ???? ??????: ' + lesson.title;
          lesson.correct_example = {
              code: 'print(\'Correct\')',
              explanation: '??? ???? ???? ???? ???????.'
          };
          lesson.wrong_example = {
              code: 'print(Wrong)',
              explanation: '??? ???? ???? ???? ??????? ???????.'
          };
          lesson.common_mistake = '????? ?????? ??? ?? ??? ??? ??????? ???? ????.';
          lesson.practical_challenge = {
              title: '????: ' + lesson.title,
              description: '?? ?????? ?? ?????? ?? ??? ?????? ??????.',
              initial_code: '# ???? ????? ???',
              expected_output: 'Success',
              hints: ['??? ??? ??????? ???????', '?????? ?????? ?????? ?????']
          };
          lesson.interactive_quiz = {
              question: '?? ?? ??????? ??????? ?? ??? ??????',
              options: ['?????? ?????', '?????? ??????', '?????? ??????', '?????? ??????'],
              correct_index: 0,
              explanation: '?????? ????? ?? ?????? ???? ???? ??????? ???????.'
          };
          updated = true;
      }
      if(!lesson.lesson_slug) {
          lesson.lesson_slug = lesson.id;
          updated = true;
      }
  }
  
  if (updated) {
      const newContent = 'const ' + file.replace('Data.ts', 'Data') + ' = ' + JSON.stringify(lessons, null, 2) + ';\nexport default ' + file.replace('Data.ts', 'Data') + ';';
      fs.writeFileSync(filePath, newContent);
      console.log('Updated', file);
  } else {
      console.log('No updates needed for', file);
  }
}

