function generateLesson(prefix, index, title, isFree) {
  const codeExamples = {
    'python': 'def practice_' + index + '():\\n    print("Welcome to ' + title + '")\\n\\npractice_' + index + '()',
    'cyber': '// Security snippet\\nconsole.log("Analyzing: ' + title + '");',
    'lang': '// Dialogue practice\\nconsole.log("Speaking: ' + title + '");'
  };

  return {
    lesson_slug: prefix + '-' + index,
    title: 'الدرس ' + index + ': ' + title,
    category: index < 40 ? "المستوى المتوسط" : (index < 80 ? "المستوى المتقدم" : "مشاريع وتطبيقات"),
    order_index: index,
    is_free: isFree,
    content_type: "theory",
    duration_minutes: 15,
    content: {
      context: 'في هذا الدرس سنناقش "' + title + '" بشكل مفصل واحترافي. يعتبر هذا الدرس أساسياً لبناء مهارتك وصقل معرفتك.',
      description: 'شرح متكامل ومبسط عن ' + title + '. سنتعرف على أفضل الممارسات وطرق التطبيق العملية خطوة بخطوة لضمان استيعابك الكامل.',
      prototype: 'Concept_' + index + '() -> Completed',
      parameters: 'المتغيرات والأدوات الأساسية المرتبطة بـ ' + title,
      return_value: 'احتراف وفهم ' + title + ' وتطبيقه في مشاريعك.',
      example: codeExamples[prefix] || '// Example for ' + title
    },
    exercise_instructions: 'قم بتطبيق ما تعلمته في درس ' + title + ' لإنشاء برنامجك الخاص.',
    expected_output: "Success"
  };
}

tracks.forEach(track => {
  const filePath = path.resolve(process.cwd(), track.file);
  if (!fs.existsSync(filePath)) {
    console.log('Not found:', filePath);
    return;
  }

  let fileContent = fs.readFileSync(filePath, 'utf8');

  let updatedCount = 0;
  for (let i = 21; i <= 100; i++) {
    const slug = track.prefix + '-' + i;
    // Regex to match the entire lesson object dynamically
    const regex = new RegExp('{\\s*"lesson_slug"\\s*:\\s*"' + slug + '"[\\s\\S]*?(?=,\\s*{\\s*"lesson_slug"|\\s*];)', 'g');
    
    const topicIndex = (i - 21) % track.topics.length;
    const topic = track.topics[topicIndex] || 'موضوع احترافي ' + i;
    const newObj = generateLesson(track.prefix, i, topic, false);
    
    if (!fileContent.match(regex)) {
        // Fallback if the array doesn't even have up to 100 lessons
        const newStr = ',\\n' + JSON.stringify(newObj, null, 2);
        fileContent = fileContent.replace(/\\s*];$/, newStr + '\\n];');
        updatedCount++;
    } else {
        const newStr = JSON.stringify(newObj, null, 2).split('\\n').map((l, idx) => idx > 0 ? '  ' + l : l).join('\\n');
        fileContent = fileContent.replace(regex, newStr);
        updatedCount++;
    }
  }

  fs.writeFileSync(filePath, fileContent, 'utf8');
  console.log('Successfully updated ' + updatedCount + ' lessons in ' + track.file);
});
